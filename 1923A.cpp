const int inf  =     1e18 + 10;
const int N    =     2e6 + 10;
int n, a[N], f, l, cnt;

void solve() {
	cin >> n;
	f = - 1, l = - 1, cnt = 0;
	for (int i = 1; i <= n; i++) {
		cin >> a[i];
		if (f == - 1 and a[i] == 1)
			f = i;
		if (a[i] == 1)
			l = i;
	}
	for (int i = f; i <= l; i++)
		cnt += a[i];
	cout << (l - f + 1) - cnt;
}

int32_t main() {
	ios_base::sync_with_stdio (0);
	cin.tie (0);
	int t;
	cin >> t;
	while (t--) {
		solve();
		cout << "\n";
	}
	return 0;
}
